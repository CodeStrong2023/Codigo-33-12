//Tipo undefined (Indefinido)
var x;
console.log(typeof x);

x = undefined;
console.log(x);

//null: ausencia de valor
var y = null; //null no es un tipo de dato, pero es un objeto
console.log(typeof y);

//Tipo de dato array 
var colores = ['azul', 'rojo', 'amarillo'];
console.log(colores);
console.log(typeof colores);

//Tipo de dato Empty Strings
var z = '';
console.log(z);
console.log(typeof z);