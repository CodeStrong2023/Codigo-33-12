// Variables y constantes

// Variables
// El uso de 'var' no se recomienda, se usa let y const

let nombre1 = 'Hector';
console.log(nombre1)

// let representa las variables, por lo que su valor puede cambiar
nombre1 = 'Marcelo';
console.log(nombre1)


// Constantes

const apellido2 = 'Perez'

// apellido2 = 'Riquelme' 
// No se puede hacer porque las constantes no se modifican