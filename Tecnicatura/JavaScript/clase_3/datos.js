// Tipos de datos en JavaScript

// Tipo string
// LAs variables se pueden reutilizar
var nombre = 'Daniel';
console.log(nombre);

// Tipo numerico
var numero = 2493;
console.log(numero);

// Tipo objeto
var objeto = {
    nombre : 'Ana',
    apellido : 'Martinez',
    edad: 23
}
console.log(objeto);

// Tipo boolean
var booleano = true;
console.log(booleano);

// Tipo funcion
function funcion(){}
console.log(funcion);

// Tipo symbol
var simbolo = Symbol('Mi simbolo')
console.log(simbolo);
